void handleMouse(int button, int state, int x, int y) {
	if (button == GLUT_LEFT_BUTTON){
	    speed += 0.1f;
	    speed4 += 0.1f;
    }
    if (button == GLUT_RIGHT_BUTTON){
	    speed -= 0.1f;
	    speed4 -= 0.1f;
    }
glutPostRedisplay();
}


void handleKeypress(unsigned char key, int x, int y) {

    switch (key) {
    case 'p':
    speed = 0.0f;
    speed4 = 0.0f;
    break;
    case 'r':
    speed = 0.1f;
    speed4 = 0.1f;
    break;
    glutPostRedisplay();
    }
}


void SpecialInput(int key, int x, int y){
    switch(key)
    {
    case GLUT_KEY_UP:
    speed = 0.5f;
    break;
    case GLUT_KEY_DOWN:
    speed = 0.1f;
    }
    glutPostRedisplay();
}


